#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""Generate system.ato validation build from board-connector.ato.

Parses BoardConnectorInterface signal names and emits system.ato that
wires control.connector.<sig> ~ main.connector.<sig> for every signal.
This gives atopile the full end-to-end circuit for constraint checking.

Usage:
    python gen_validation.py

Output: hardware/boards/elec/src/system.ato (auto-generated, do not edit)
"""

import re
import os

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
BOARDS_DIR = os.path.normpath(os.path.join(SCRIPT_DIR, "..", ".."))
CONNECTOR_FILE = os.path.join(BOARDS_DIR, "elec", "src", "board-connector.ato")
OUTPUT_FILE = os.path.join(BOARDS_DIR, "elec", "src", "system.ato")


def extract_signals(filepath):
    """Extract signal names from BoardConnectorInterface module."""
    signals = []
    in_module = False
    with open(filepath) as f:
        for line in f:
            stripped = line.strip()
            if stripped.startswith("module BoardConnectorInterface"):
                in_module = True
                continue
            if not in_module:
                continue
            # Stop at the next module definition
            if stripped.startswith("module "):
                break
            # Match "signal <name>" lines
            match = re.match(r"signal\s+(\w+)", stripped)
            if match:
                signals.append(match.group(1))
    return signals


def generate_system_ato(signals):
    """Generate system.ato content."""
    lines = [
        "# System Validation Build — AUTO-GENERATED, DO NOT EDIT",
        "# Generated by scripts/gen_validation.py from board-connector.ato",
        "# Wires control and main boards together through connector interface",
        "# for full end-to-end constraint checking by atopile",
        "",
        'from "control.ato" import Control',
        'from "main.ato" import Main',
        "",
        "module System:",
        "    control = new Control",
        "    main = new Main",
        "",
        "    # Wire every connector signal between boards",
    ]
    for sig in signals:
        lines.append(f"    control.connector.{sig} ~ main.connector.{sig}")
    lines.append("")
    return "\n".join(lines)


def main():
    signals = extract_signals(CONNECTOR_FILE)
    print(f"Found {len(signals)} signals in BoardConnectorInterface")

    content = generate_system_ato(signals)

    # Only write if content changed — avoids triggering unnecessary ato rebuild
    try:
        with open(OUTPUT_FILE) as f:
            existing = f.read()
        if existing == content:
            print(f"No changes to {OUTPUT_FILE}")
            return
    except FileNotFoundError:
        pass

    with open(OUTPUT_FILE, "w") as f:
        f.write(content)
    print(f"Generated {OUTPUT_FILE}")


if __name__ == "__main__":
    main()
